		<p>We must ask Captain Nemo, said Conseil. We are accustomed to them, replied Captain Nemo, and in time you will be too. However, we shall be armed, and on the road we may be able to hunt some of the tribe. It is interesting. So, till to-morrow, sir, and early. The boat put off, and lifted by the six rowers, drew rapidly toward the dugong, which floated about two miles from the Nautilus. The Scotia had not struck, but she had been struck, and seemingly by something rather sharp and penetrating than blunt. The shock had been so light that no one had been alarmed, had it not been for the shouts of the carpenter's watch, who rushed on to the bridge, exclaiming, We are sinking! We are sinking! At first the passengers were much frightened, but Captain Anderson hastened to reassure them. The danger could not be imminent. The Scotia, divided into seven compartments by strong partitions, could brave with impunity any leak. Captain Anderson went down immediately into the hold. He found that the sea was pouring into the fifth compartment; and the rapidity of the influx proved that the force of the water was considerable. Fortunately this compartment did not hold the boilers, or the fires would have been immediately extinguished. Captain Anderson ordered the engines to be stopped at once, and one of the men went down to ascertain the extent of the injury. Some minutes afterward they discovered the existence of a large hole, of two yards in diameter, in the ship's bottom. Such a leak could not be stopped; and the Scotia, her paddles half submerged, was obliged to continue her course. She was then three hundred miles from Cape Clear, and after three days' delay, which caused great uneasiness in Liverpool, she entered the basin of the company.</p>
